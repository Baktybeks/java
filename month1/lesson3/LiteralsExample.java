public class LiteralsExample {
  public static void main(String[] args) {
    int intNumber = 42;
    long longNumber = 123456789L;
    double doubleNumber = 3.14;
    char letter = 'A';
    String text = "Java";
    boolean isJavaFun = true;
    System.out.println("Целое (int): " + intNumber);
    System.out.println("Целое (long): " + longNumber);
    System.out.println("Дробное: " + doubleNumber);
    System.out.println("Символ: " + letter);
    System.out.println("Строка: " + text);
    System.out.println("Логическое: " + isJavaFun);
  }
}
